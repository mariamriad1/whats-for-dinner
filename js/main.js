function displayRecipe(recipe) {
  var i = Math.floor(Math.random() * recipes.length);
  var recipe = recipes[i];
  document.getElementById("badge1").innerHTML = recipe.badge1;
  document.getElementById("badge2").innerHTML = recipe.badge2;
  document.getElementById("recipe").innerHTML = recipe.name;
  document.getElementById("recipe_desc").innerHTML = recipe.describtion;
  document.getElementById("time").innerHTML = recipe.time;
  document.getElementById("time2").innerHTML = recipe.time2;
  document.getElementById("rating").innerHTML = recipe.rating[0];
  document.getElementById("reviews").innerHTML = recipe.rating[1];
  const tipsContainer = document.getElementById("tips-container");
  tipsContainer.innerHTML = "";
  for (let i = 0; i < recipe.tips.length; i++) {
    tipsContainer.innerHTML += `
        <div class="tip-box">
            <i class="fa-solid fa-circle-check"></i>
            <span>${recipe.tips[i]}</span>
        </div>
    `;
  }
  const prepBox = document.getElementById("prep-box");
  const prepText = document.getElementById("Extended_Preparation_Time");

  if (recipe.Extended_Preparation_Time) {
    prepBox.style.display = "flex";
    prepText.innerHTML = recipe.Extended_Preparation_Time;
  } else {
    prepBox.style.display = "none";
  }

  document.getElementById("people").innerHTML = recipe.people;
  document.getElementById("recipe-image").src = recipe.image;
  var instructionsContainer = document.getElementById("instructions");
  instructionsContainer.innerHTML = "";

  for (var j = 0; j < recipe.instructions.length; j++) {
    instructionsContainer.innerHTML += `
<div class="instruction d-flex gap-3 align-items-center p-3">
    <div
        class="id rounded-4 fw-bold fs-6 text-light d-flex justify-content-center align-items-center p-4">
        ${j + 1}
    </div>

    <div class="text">
        ${recipe.instructions[j]}
    </div>
</div>
`;
  }
  var ingredientsContainer = document.getElementById("ingredients");
  ingredientsContainer.innerHTML = "";
  for (var k = 0; k < recipe.ingredients.length; k++) {
    ingredientsContainer.innerHTML += `
    <div class="ingredient d-flex gap-3">
                        <div
                          class="id rounded-circle fw-bold fs-6 text-light d-flex justify-content-center align-items-center"
                        >
                          ${k + 1}
                        </div>
                        <div class="text">${recipe.ingredients[k]}</div>
                      </div>`;
  }
  for (var key in recipe.nutrition) {
    document.getElementById(key).innerHTML = recipe.nutrition[key];
  }
}

displayRecipe(recipes[0]);

document.getElementById("try-another").onclick = function () {
  var i = Math.floor(Math.random() * recipes.length);
  displayRecipe(recipes[i]);
};
